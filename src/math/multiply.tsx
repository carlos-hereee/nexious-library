/** *
 * function multiply
 * @param {number} num1 The fist number
 * @param {number} num2 The secondary number
 * @returns {number} Returns the product of num1 and num2
 * @example
 * multiply (3, 6) => 18
 * multiply (4, 5) => 20
 * multiply (1, 6) => 6
 */
export const multiply = (num1: number, num2: number): number => num1 * num2;
