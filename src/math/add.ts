/***
 * function addition
 * @param {number} num1 The fist number
 * @param {number} num2 The secondary number
 * @returns {number} Returns the sum of num1 and num2
 * @example
 * add(3, 6) => 9
 * add(4, 5) => 9
 * add(1, 6) => 7
 */
export const add = (num1: number, num2: number): number => num1 + num2;
